#Rubiks cube volume calculator
print("Hi, lets calculate the volume of your rubiks cube")
Lenght = int(input("How long is the the cube?"))
Height = int(input("how tall is the cube?"))
Width = int(input("How width is the cube?"))
print ("The Height is {} The Width is {} The Lenght is {}".format (Height, Width, Lenght))
print("The Volume of the Rubik Cube is {}".format(Height * Width * Lenght))